package com.decred.memories.payload.response;

public class SourceResponse {

	private String id;
	private String name;
	
	public SourceResponse() {}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
